                            <hr>

                            <P>Na Administração Pública, a grande maioria dos atos oficiais deve ser efetivada mediante um processo administrativo, pois, desse modo, é possível identificar os agentes envolvidos, dar transparência às ações, rastrear o encadeamento de atos e, principalmente, identificar os motivos e motivações. Segundo Nohara e Marrara (2018),</P>
                            <P>O termo processo, derivado da palavra latina pro cedere, significa curso ou marcha para frente (pela presença do prefixo pro). Indica, portanto, a ação de avançar ou ir para frente. Processo em sentido amplo representa o conjunto sequencial das ações que objetivam alcançar determinado fim (p. 74-75).</P>
                            <P>Assim, entendemos que, para que cheguemos às conclusões de nossos objetivos na Administração Pública, é necessário formalizar, mediante processo, todos os passos tomados até que se consiga o resultado desejado (ato administrativo). São exemplos de processos administrativos, portanto, desde os processos licitatórios até o requerimento de alteração do período de gozo de férias efetuado por um servidor. Processos Administrativos, portanto, são aqueles que ocorrem no âmbito do exercício da função administrativa pelo poder público.</P>
